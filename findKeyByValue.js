const findKeyByValue = function(obj, val) {
  for (const item of Object.keys(obj)) {
    if (obj[item] === val) {
      return item;
    }
  }
};

const bestTVShowsByGenre = {
  sciFi: "The Expanse",
  comedy: "Brooklyn Nine-Nine",
  drama:  "The Wire"
};

assertEqual(findKeyByValue(bestTVShowsByGenre, "The Wire"), "drama");
